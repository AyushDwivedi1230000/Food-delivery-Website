// Generate random calories based on food type
export const getCaloriesForFood = (itemName: string): number => {
  const name = itemName.toLowerCase();
  
  if (name.includes('salad')) return Math.floor(Math.random() * (250 - 120) + 120);
  if (name.includes('burger')) return Math.floor(Math.random() * (750 - 450) + 450);
  if (name.includes('pizza')) return Math.floor(Math.random() * (600 - 350) + 350);
  if (name.includes('dessert') || name.includes('cake') || name.includes('ice cream')) 
    return Math.floor(Math.random() * (500 - 250) + 250);
  if (name.includes('drink') || name.includes('juice')) return Math.floor(Math.random() * (200 - 50) + 50);
  
  return Math.floor(Math.random() * (650 - 200) + 200);
};

